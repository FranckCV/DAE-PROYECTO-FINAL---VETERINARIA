    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ResultSet rsRaza = null;

        try {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar un código para buscar");
            } else {
                for (int i = 0; i < tblRaza.getRowCount(); i++) {
                    String valorCodigo = tblRaza.getValueAt(i, 0).toString();
                    if (valorCodigo.equals(txtId.getText())) {
                        tblRaza.setRowSelectionInterval(i, i);
                        tblRaza.scrollRectToVisible(tblRaza.getCellRect(i, 0, true));
                        break;
                    }
                }
                rsRaza = objRaza.buscarRaza(Integer.parseInt(txtId.getText()));
                Utilidad.desactivarFields(txtId, txtId, txtNombre);
                cboEspecie.setEnabled(false);
                if (rsRaza.next()) {
                    txtNombre.setText(rsRaza.getString("nombre"));
                    cboEspecie.setSelectedItem(objRaza.obtenerNombreEspecie(rsRaza.getInt("especie_id")));
                    chkVigencia.setSelected(rsRaza.getBoolean("disponibilidad"));
                    rsRaza.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Codigo de especie no existente");
                    limpiarControles();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error " + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

