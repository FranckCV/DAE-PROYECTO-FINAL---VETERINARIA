private void mostrar() {
    try {
        Container contenedor = this.report;
        contenedor.setLayout(new BorderLayout());
        contenedor.removeAll();
        Map<String, Object> parametros = new HashMap<>();
        java.util.Date fechaInicio = dtcInicio.getDate(); // Fecha de inicio
        java.util.Date fechaFin = dtcFin.getDate();       // Fecha de fin

        if (fechaInicio == null || fechaFin == null) {
            JOptionPane.showMessageDialog(
                this,
                "Por favor, selecciona ambas fechas (Inicio y Fin).",
                "Error",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        if (fechaInicio.after(fechaFin)) {
            JOptionPane.showMessageDialog(
                this,
                "La fecha de inicio no puede ser posterior a la fecha de fin.",
                "Error",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        parametros.put("fechaInicio", new java.sql.Date(fechaInicio.getTime())); // Convertir a java.sql.Date
        parametros.put("fechaFin", new java.sql.Date(fechaFin.getTime()));
        JRViewer objReporte = new clsReporte().reporteInterno(docReporte + ".jasper", parametros);
        contenedor.add(objReporte);
        contenedor.revalidate();
        contenedor.repaint();

        report.setPreferredSize(dimension);
        this.report.setVisible(true);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(
                this,
                "Error en Reporte: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
        );
    }
}

    
